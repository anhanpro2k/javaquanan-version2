    private void jlbDangNhapMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlbDangNhapMousePressed
        jlbDangNhap.setIcon(new ImageIcon(getClass().getResource("../img/icon/login-button.png")));
        String tenTaiKhoan = jtfTaiKhoan.getText();
        String matKhau = jpwMatKhau.getText();
        if (tenTaiKhoan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên tài khoản");
            return;
        } else if (matKhau.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mật khẩu");
            return;
        }
        if (kiemTraTaiKhoan(tenTaiKhoan, matKhau)) {
            JOptionPane.showMessageDialog(this, "Đăng nhập thành công!");
            System.out.println(NhanVienBus.nhanVienDangNhap.getTenNV());
            mainGUI = new MainJFormGUI();
            mainGUI.setVisible(true);
            qlquanan.QLQuanAn.dangNhapGUI.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(this, "Đăng nhập thất bại! Sai tên tài khoản hoặc mật khẩu.");

        }
    }//GEN-LAST:event_jlbDangNhapMousePressed

