    private void loadData() {
        int i = nguyenLieuCanSua.getTen().indexOf("(");
        jtfTenNguyenLieu.setText(nguyenLieuCanSua.getTen().substring(0, i - 1));
        String tenDonVi = nguyenLieuCanSua.getTen().substring(i + 1, nguyenLieuCanSua.getTen().length() - 1);
        for (int index = 0; index < jcbDonVi.getItemCount(); index++) {
            if (jcbDonVi.getItemAt(index).toString().equals(tenDonVi)) {
                jcbDonVi.setSelectedIndex(index);
                break;
            }
        }
        jtfDonGia.setText(nguyenLieuCanSua.getGia() + "");
    }

    private int traVeViTriDauNgoac(String tenNguyenLieu) {
        for (int i = 0; i < tenNguyenLieu.length(); i++) {
            if (tenNguyenLieu.charAt(i) == ')') {
                return i;
            }
        }
        return -1;
    }

