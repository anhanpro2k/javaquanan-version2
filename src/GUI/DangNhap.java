    private boolean kiemTraTaiKhoan(String tenTaiKhoan, String matKhau) {

        for (TaiKhoanDTO taiKhoan : TaiKhoanBUS.danhSachTaiKhoan) {
            if (taiKhoan.getTenTK().equals(tenTaiKhoan) && taiKhoan.getMatKhau().equals(matKhau)) {
                NhanVienBus.nhanVienDangNhap = new NhanVienBus().getNhanVienByMaTaiKhoan(taiKhoan.getMaTK());
                return true;
            }
        }
        return false;
    }

